

// What is Parameter & Argument:

// Term	Meaning

// Parameter:   Function define karte waqt jo variables dete hain

// Argument :	Function call karte waqt jo actual values dete hain




function greet(name) {   // name is a parameter
    console.log("Hello " + name);
}

greet("Nishant"); // "Nishant" is an argument


// output : Hello Nishant

