// Print numbers from 1 to 10 using for loop
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// Print numbers from 1 to 10 using while loop
let i = 1;

while (i <= 10) {
    console.log(i);
    i++;  // increment karna mat bhoolna
}


// Print numbers from 1 to 10 using do-while loop
let j = 1;

do {
    console.log(j);
    j++;  // pehle run, baad me condition check
} while (j <= 10);
